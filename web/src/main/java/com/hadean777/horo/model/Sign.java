package com.hadean777.horo.model;


public class Sign {
	
	private String name;
	private String displayName;
	private String description;
	private Short startDay;
	private Short startMonth;
	private Short endDay;
	private Short endMonth;
	
	/**
	 * Default constructor
	 */
	public Sign(){
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Short getStartDay() {
		return startDay;
	}
	public void setStartDay(Short startDay) {
		this.startDay = startDay;
	}
	public Short getStartMonth() {
		return startMonth;
	}
	public void setStartMonth(Short startMonth) {
		this.startMonth = startMonth;
	}
	public Short getEndDay() {
		return endDay;
	}
	public void setEndDay(Short endDay) {
		this.endDay = endDay;
	}
	public Short getEndMonth() {
		return endMonth;
	}
	public void setEndMonth(Short endMonth) {
		this.endMonth = endMonth;
	}
	
	
	

}
